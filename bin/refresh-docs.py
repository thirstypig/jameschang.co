#!/usr/bin/env python3
"""Regenerate the LIVING docs in the portfolio docs hub from real data.

Run before every push (or wire a pre-push hook):

    python3 bin/refresh-docs.py

Generates:
  admin/docs/under-the-hood/stats.md      docs + portfolio + repo dashboard (6a)
  admin/docs/projects/<slug>/costs.md     unit-economics table from costs.config.json (6b)
  admin/docs/README-DOCS.md               status block between DOCS:STATUS markers (6d)

NOT generated here: a public system-status listing integration env-key names — that is
an attack map and violates the hub's public-safe rule. Service health / expiry status
belongs in the local/encrypted admin track, never on this public page.

Python 3 stdlib only. LOC is a rough vanity signal, tracked for reference — real
progress is features/phases shipped.
"""
import json
import os
import re
import subprocess
from collections import Counter
from datetime import datetime, timezone

REPO_ROOT = os.path.normpath(os.path.join(os.path.dirname(__file__), ".."))
HUB = os.path.join(REPO_ROOT, "admin", "docs")
STATS_MD = os.path.join(HUB, "under-the-hood", "stats.md")
PORTFOLIO_JSON = os.path.join(REPO_ROOT, "admin", "portfolio.json")
README = os.path.join(HUB, "README-DOCS.md")

_FM_FIELD = re.compile(r"^(id|type|status|project):\s*(.+?)\s*$", re.MULTILINE)


def parse_frontmatter(path):
    """Return {id,type,status,project} from a doc's opening YAML block, or {}."""
    try:
        with open(path, encoding="utf-8") as f:
            head = f.read(2000)
    except OSError:
        return {}
    if not head.startswith("---"):
        return {}
    end = head.find("\n---", 3)
    if end == -1:
        return {}
    block = head[3:end]
    return {k: v for k, v in _FM_FIELD.findall(block)}


def collect_docs():
    """All authored hub docs with frontmatter, excluding templates + generated/stub."""
    skip_dirs = {"_templates"}
    skip_names = {"INBOX.md", "stats.md"}
    out = []
    for root, dirs, files in os.walk(HUB):
        dirs[:] = [d for d in dirs if d not in skip_dirs]
        for fn in files:
            if not fn.endswith(".md") or fn in skip_names or fn.startswith("_"):
                continue
            fm = parse_frontmatter(os.path.join(root, fn))
            if fm.get("type"):
                out.append(fm)
    return out


def git_tracked_files():
    try:
        out = subprocess.run(
            ["git", "-C", REPO_ROOT, "ls-files"],
            capture_output=True, text=True, check=True,
        ).stdout
        return [f for f in out.splitlines() if f]
    except (subprocess.CalledProcessError, FileNotFoundError):
        return []


def repo_stats(files):
    by_ext = Counter()
    loc_by_ext = Counter()
    for rel in files:
        ext = os.path.splitext(rel)[1].lower() or "(none)"
        by_ext[ext] += 1
        path = os.path.join(REPO_ROOT, rel)
        try:
            with open(path, "rb") as f:
                loc_by_ext[ext] += sum(1 for _ in f)
        except OSError:
            pass
    return by_ext, loc_by_ext


def portfolio_status():
    try:
        with open(PORTFOLIO_JSON, encoding="utf-8") as f:
            projects = json.load(f).get("projects", [])
    except (OSError, json.JSONDecodeError):
        return Counter(), 0
    return Counter(p.get("pm_status", "?") for p in projects), len(projects)


def _table(rows, headers):
    out = ["| " + " | ".join(headers) + " |",
           "|" + "|".join("---" for _ in headers) + "|"]
    for r in rows:
        out.append("| " + " | ".join(str(c) for c in r) + " |")
    return "\n".join(out)


def gen_stats(now):
    docs = collect_docs()
    by_type = Counter(d.get("type", "?") for d in docs)
    by_status = Counter(d.get("status", "?") for d in docs)
    by_project = Counter(d.get("project", "?") for d in docs)
    files = git_tracked_files()
    by_ext, loc_by_ext = repo_stats(files)
    pm_counts, n_projects = portfolio_status()

    prds_per_project = Counter(
        d.get("project") for d in docs if d.get("type") == "prd")
    covered = sum(1 for _ in prds_per_project)

    top_ext = sorted(loc_by_ext.items(), key=lambda kv: kv[1], reverse=True)[:8]

    lines = [
        "---", "id: DOC-STATS", "type: stats", "project: portfolio",
        "status: active", "owner: james", "tags: []", "links: []",
        f"updated: {now:%Y-%m-%d}", "---", "",
        "<!-- GENERATED by bin/refresh-docs.py — do not edit by hand. -->", "",
        "# Stats",
        "",
        f"_Regenerated {now:%Y-%m-%d %H:%M UTC}. LOC is a rough vanity signal, "
        "tracked for reference — real progress is features/phases shipped._",
        "",
        "## Docs hub",
        "",
        f"- **{len(docs)}** authored docs · **{covered}** of **{n_projects}** "
        "projects have at least one PRD",
        "",
        "**By type**",
        _table(sorted(by_type.items()), ["type", "count"]),
        "",
        "**By status**",
        _table(sorted(by_status.items()), ["status", "count"]),
        "",
        "**By project**",
        _table(sorted(by_project.items()), ["project", "docs"]),
        "",
        "## Portfolio",
        "",
        f"- **{n_projects}** projects on the board",
        _table(sorted(pm_counts.items()), ["pm-status", "count"]),
        "",
        "## Repo (jameschang.co)",
        "",
        f"- **{len(files)}** tracked files · "
        f"**{sum(loc_by_ext.values()):,}** total lines",
        "",
        "**Lines by file type (top 8)**",
        _table([(e, f"{n:,}") for e, n in top_ext], ["ext", "lines"]),
        "",
    ]
    _write(STATS_MD, "\n".join(lines).rstrip() + "\n")
    return len(docs), n_projects


def _fmt(x):
    return f"${x:,.2f}"


def gen_costs(now):
    """One costs.md per project that has a costs.config.json."""
    written = []
    projects_dir = os.path.join(HUB, "projects")
    if not os.path.isdir(projects_dir):
        return written
    for slug in sorted(os.listdir(projects_dir)):
        cfg_path = os.path.join(projects_dir, slug, "costs.config.json")
        if not os.path.isfile(cfg_path):
            continue
        try:
            with open(cfg_path, encoding="utf-8") as f:
                cfg = json.load(f)
        except (OSError, json.JSONDecodeError):
            continue
        _write(os.path.join(projects_dir, slug, "costs.md"),
               _costs_md(slug, cfg, now))
        written.append(slug)
    return written


def _costs_md(slug, cfg, now):
    a = cfg.get("assumptions", {})
    u = cfg.get("unitCosts", {})
    verify = set(cfg.get("_verify", []))

    def vflag(key):
        return "  **(VERIFY)**" if key in verify else ""

    plan = a.get("planPriceUsd", 0) or 0
    units = a.get("unitsPerUserPerMonth", 0) or 0
    per_unit = u.get("primaryVariablePerUnit", 0) or 0
    auth = u.get("perUserMonthAuth", 0) or 0
    db = u.get("perUserMonthDb", 0) or 0
    hosting = u.get("hostingFlatMonth", 0) or 0
    pct = u.get("paymentPctFee", 0) or 0
    flat = u.get("paymentFlatFee", 0) or 0

    var_per_user = per_unit * units + auth + db
    pay_per_user = plan * pct + flat if plan else 0

    rows = []
    for n in cfg.get("tiers", []):
        revenue = n * plan
        total_var = n * var_per_user
        total_pay = n * pay_per_user
        margin = revenue - total_var - hosting - total_pay
        margin_pct = (margin / revenue * 100) if revenue else 0
        rows.append((f"{n:,}", _fmt(var_per_user), _fmt(total_var),
                     _fmt(hosting), _fmt(total_pay), _fmt(revenue),
                     _fmt(margin), f"{margin_pct:.0f}%"))

    lines = [
        "---", "id: DOC-COSTS-" + slug, "type: costs", f"project: {slug}",
        "status: draft", "owner: james", "tags: [payments]", "links: []",
        f"updated: {now:%Y-%m-%d}", "---", "",
        "<!-- GENERATED by bin/refresh-docs.py from costs.config.json. "
        "Edit the config, not this file. -->", "",
        f"# {slug} — unit economics",
        "",
        "_Numbers flagged **(VERIFY)** are unconfirmed inputs — never present them "
        "as fact until confirmed and removed from `_verify` in costs.config.json._",
        "",
        "## Assumptions",
        "",
        f"- Plan price: {_fmt(plan)}/user/month{vflag('planPriceUsd')}",
        f"- Usage: {units} {a.get('unitLabel','units')}/user/month"
        f"{vflag('unitsPerUserPerMonth')}, "
        f"avg {a.get('avgUnitSize',0)} {a.get('unitSizeLabel','')}"
        f"{vflag('avgUnitSize')}",
        f"- Primary variable: {_fmt(per_unit)}/{a.get('unitLabel','unit')}"
        f"{vflag('primaryVariablePerUnit')}",
        f"- Auth: {_fmt(auth)}/user/mo{vflag('perUserMonthAuth')} · "
        f"DB: {_fmt(db)}/user/mo{vflag('perUserMonthDb')} · "
        f"Hosting: {_fmt(hosting)}/mo flat{vflag('hostingFlatMonth')}",
        f"- Payments: {pct * 100:.1f}% + {_fmt(flat)} per charge",
        "",
        "## Margin by tier",
        "",
        _table(rows, ["users", "var/user/mo", "total var", "hosting",
                      "pay fees", "revenue", "gross margin $", "margin %"]),
        "",
    ]
    return "\n".join(lines).rstrip() + "\n"


def update_readme_status(n_docs, n_projects, now):
    start = "<!-- DOCS:STATUS:START -->"
    end = "<!-- DOCS:STATUS:END -->"
    pm_counts, _ = portfolio_status()
    pm_line = " · ".join(f"{v} {k}" for k, v in sorted(pm_counts.items()))
    block = (
        f"{start}\n"
        f"**Hub status** (auto · {now:%Y-%m-%d}): {n_docs} docs · "
        f"{n_projects} projects — {pm_line}. "
        f"Regenerate with `python3 bin/refresh-docs.py`.\n"
        f"{end}"
    )
    try:
        with open(README, encoding="utf-8") as f:
            content = f.read()
    except OSError:
        return
    if start in content and end in content:
        content = re.sub(re.escape(start) + r".*?" + re.escape(end),
                         block, content, flags=re.DOTALL)
    else:
        content = content.rstrip() + "\n\n" + block + "\n"
    _write(README, content)


def _write(path, text):
    os.makedirs(os.path.dirname(path), exist_ok=True)
    with open(path, "w", encoding="utf-8") as f:
        f.write(text)



def _scan_docs_full():
    """Walk the hub reading BOTH frontmatter and body — the PM review needs the
    open-decision / [unknown] / risk signals that live in the doc bodies."""
    import glob
    out = []
    for path in glob.glob(os.path.join(HUB, "**", "*.md"), recursive=True):
        base = os.path.basename(path)
        if base.startswith("_") or "/_templates/" in path.replace("\\", "/"):
            continue
        try:
            with open(path, encoding="utf-8") as f:
                content = f.read()
        except OSError:
            continue
        if not content.startswith("---"):
            continue
        end = content.find("\n---", 3)
        if end == -1:
            continue
        fmblock, body = content[3:end], content[end + 4:]
        fm = {}
        for line in fmblock.splitlines():
            m = re.match(r"^(\w+):\s*(.*)$", line)
            if m:
                fm[m.group(1)] = m.group(2).strip()
        if not fm.get("type"):
            continue
        out.append({
            "fm": fm,
            "project": fm.get("project", "?"),
            "type": fm.get("type", ""),
            "stage": fm.get("stage", ""),
            "title": (re.search(r"^#\s+(.+)$", re.sub(r"```.*?```", "", body, flags=re.S), re.M) or [None, base])[1],
            "unknowns": len(re.findall(r"\[unknown\]", body, re.I)),
            "blockers": re.findall(r"^- \[ \]\s*(.+)$", body, re.M),
            "risks": re.findall(r"^\| (RISK-\d+) \| ([^|]+?) \|", body, re.M),
        })
    return out


def gen_pm_review(now):
    docs = _scan_docs_full()
    pm_counts, n_projects = portfolio_status()
    try:
        with open(PORTFOLIO_JSON, encoding="utf-8") as f:
            pstatus = {p["slug"]: p.get("pm_status", "?")
                       for p in json.load(f).get("projects", [])}
    except (OSError, json.JSONDecodeError):
        pstatus = {}

    projects = sorted(set(d["project"] for d in docs if d["project"] != "portfolio"))
    total_unknowns = sum(d["unknowns"] for d in docs)
    blockers = [(d["project"], b) for d in docs for b in d["blockers"]]
    risks = [(d["project"], rid, txt.strip()) for d in docs for (rid, txt) in d["risks"]]

    lines = [
        "---", "id: DOC-PM-REVIEW", "type: status", "project: portfolio",
        "status: active", "owner: james", "tags: []", "links: []",
        f"updated: {now:%Y-%m-%d}", "---", "",
        "<!-- GENERATED by bin/refresh-docs.py — the PM cockpit. Do not edit by hand. -->",
        "", "# PM review — the portfolio cockpit",
        "",
        f"_Regenerated {now:%Y-%m-%d %H:%M UTC}. What needs your decision, across every "
        "project — so you run reviews from here, not from 38 separate docs._",
        "",
        "## At a glance",
        "",
        f"- **{n_projects}** projects · **{total_unknowns}** open questions · "
        f"**{len(blockers)}** launch blockers · **{len(risks)}** open risks",
        "",
    ]

    if blockers:
        lines += ["## ⚠ Launch blockers (decide before shipping)", ""]
        for proj, b in blockers:
            lines.append(f"- **{proj}** — {b}")
        lines.append("")

    lines += ["## Features by stage (MVP → shipped → planned)", ""]
    staged = [d for d in docs if d["type"] == "prd" and d["stage"]]
    if staged:
        by_proj = {}
        for d in staged:
            by_proj.setdefault(d["project"], []).append(d)
        for proj in sorted(by_proj):
            lines.append(f"**{proj}**")
            for stage in ("mvp", "shipped", "planned"):
                items = [d["title"] for d in by_proj[proj] if d["stage"] == stage]
                if items:
                    lines.append(f"- _{stage}_: " + " · ".join(items))
            lines.append("")
    else:
        lines += ["_No PRDs are stage-tagged yet (add `stage: mvp|shipped|planned`)._", ""]

    lines += ["## Open questions by project", "",
              _table(
                  [(p,
                    sum(d["unknowns"] for d in docs if d["project"] == p),
                    pstatus.get(p, "?"))
                   for p in projects if sum(d["unknowns"] for d in docs if d["project"] == p)],
                  ["project", "open [unknown]s", "pm-status"]),
              ""]

    if risks:
        lines += ["## Open risks", ""]
        for proj, rid, txt in risks:
            lines.append(f"- **{proj}** {rid} — {txt}")
        lines.append("")

    lines += ["## Per-project scorecard", "",
              _table(
                  [(p, pstatus.get(p, "?"),
                    sum(1 for d in docs if d["project"] == p and d["type"] == "prd"),
                    sum(d["unknowns"] for d in docs if d["project"] == p))
                   for p in projects],
                  ["project", "pm-status", "PRDs", "open [unknown]s"]),
              ""]

    _write(os.path.join(HUB, "under-the-hood", "pm-review.md"),
           "\n".join(lines).rstrip() + "\n")


def main():
    now = datetime.now(timezone.utc)
    n_docs, n_projects = gen_stats(now)
    costs = gen_costs(now)
    update_readme_status(n_docs, n_projects, now)
    gen_pm_review(now)
    # Rebuild the docs-board manifest the viewer reads.
    subprocess.run(["python3",
                    os.path.join(os.path.dirname(__file__), "build-docs-index.py")],
                   check=False)
    print(f"Refreshed: stats.md · costs.md for {costs or '(none)'} · "
          f"README status block. {n_docs} docs, {n_projects} projects.")


if __name__ == "__main__":
    main()
