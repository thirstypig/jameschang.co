#!/usr/bin/env python3
"""Sync the docs-board comment inbox.

Reads admin/docs/_comments.json and regenerates admin/docs/INBOX.md — the list of
OPEN comments to act on at the start of a session: change_requests first, then
questions, then notes; newest first within each. Resolved comments drop off the
inbox (a short "recently resolved" tail shows they closed).

Python 3 stdlib only, matching this repo's other bin/*.py scripts.

TODO: today this reads a local JSON stub. When the docs board writes comments to a
real store (per a project's stack), point COMMENTS_FILE at that source instead —
the rest of this script is source-agnostic.
"""
import json
import os
from datetime import datetime, timezone

REPO_ROOT = os.path.normpath(os.path.join(os.path.dirname(__file__), ".."))
COMMENTS_FILE = os.path.join(REPO_ROOT, "admin", "docs", "_comments.json")
INBOX_FILE = os.path.join(REPO_ROOT, "admin", "docs", "INBOX.md")

KIND_ORDER = ["change_request", "question", "note"]
KIND_LABEL = {
    "change_request": "Change requests",
    "question": "Questions",
    "note": "Notes",
}
ACTIONABLE = {"open", "in_review"}


def load_comments():
    try:
        with open(COMMENTS_FILE, encoding="utf-8") as f:
            return json.load(f).get("comments", [])
    except (OSError, json.JSONDecodeError):
        return []


def _created(c):
    return c.get("created", "")


def build_inbox(comments, now):
    actionable = [c for c in comments if c.get("status") in ACTIONABLE]
    resolved = [c for c in comments if c.get("status") == "resolved"]

    lines = [
        "---",
        "id: DOC-INBOX",
        "type: note",
        "project: portfolio",
        "status: active",
        "owner: james",
        "tags: []",
        "links: []",
        f"updated: {now:%Y-%m-%d}",
        "---",
        "",
        "<!-- GENERATED by bin/sync-inbox.py — do not edit by hand. -->",
        "",
        "# Inbox",
        "",
        f"_{len(actionable)} open · {len(resolved)} resolved · "
        f"regenerated {now:%Y-%m-%d %H:%M UTC}_",
        "",
        "Start each session here: act on **change requests**, answer **questions**,",
        "then resolve them (status → resolved, with a note) so they clear.",
        "",
    ]

    if not actionable:
        lines += ["**Inbox zero.** Nothing open. ✨", ""]

    for kind in KIND_ORDER:
        items = sorted(
            (c for c in actionable if c.get("kind") == kind),
            key=_created, reverse=True,
        )
        if not items:
            continue
        lines.append(f"## {KIND_LABEL[kind]} ({len(items)})")
        lines.append("")
        for c in items:
            when = c.get("created", "")[:10]
            flag = " · _in review_" if c.get("status") == "in_review" else ""
            lines.append(
                f"- **[{c.get('doc', '?')}]** ({c.get('id', '?')} · {when}{flag}) "
                f"— {c.get('body', '').strip()}"
            )
        lines.append("")

    if resolved:
        lines.append(f"## Recently resolved ({len(resolved)})")
        lines.append("")
        for c in sorted(resolved, key=_created, reverse=True)[:5]:
            res = c.get("resolution") or {}
            link = res.get("link", "")
            link_str = f" → {link}" if link else ""
            lines.append(
                f"- ~~[{c.get('doc', '?')}] {c.get('id', '?')}~~ "
                f"— {res.get('note', '')}{link_str}"
            )
        lines.append("")

    return "\n".join(lines).rstrip() + "\n"


def main():
    comments = load_comments()
    now = datetime.now(timezone.utc)
    with open(INBOX_FILE, "w", encoding="utf-8") as f:
        f.write(build_inbox(comments, now))
    opened = sum(1 for c in comments if c.get("status") in ACTIONABLE)
    print(f"Inbox synced: {opened} open comment(s) → "
          f"{os.path.relpath(INBOX_FILE, REPO_ROOT)}")


if __name__ == "__main__":
    main()
